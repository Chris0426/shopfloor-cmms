def negotiate_locale(cookie_locale: str | None, accept_language: str | None) -> str:
    """決定當前 locale:cookie(使用者明選,ADR-023 暫存)優先 → Accept-Language → 預設 en。

    未登入頁用 Accept-Language best-effort;登入後 ADR-023 改以 user_account.ui_locale 覆蓋。
    """
    if cookie_locale in SUPPORTED_LOCALES:
        return cookie_locale
    for part in _parse_accept_language(accept_language):
        if part in SUPPORTED_LOCALES:
            return part
        # 語言前綴寬鬆匹配(zh → zh-TW;en-US → en)
        prefix = part.split("-", 1)[0]
        for supported in SUPPORTED_LOCALES:
            if supported == prefix or supported.split("-", 1)[0] == prefix:
                return supported
    return DEFAULT_LOCALE


def _parse_accept_language(header: str | None) -> list[str]:
    """粗解析 Accept-Language 為依 q 值排序的 tag 清單(best-effort,不引第三方)。"""
    if not header:
        return []
    items: list[tuple[float, str]] = []
    for chunk in header.split(","):
        chunk = chunk.strip()
        if not chunk:
            continue
        tag, _, q = chunk.partition(";q=")
        try:
            weight = float(q) if q else 1.0
        except ValueError:
            weight = 1.0
        items.append((weight, tag.strip()))
    items.sort(key=lambda it: it[0], reverse=True)
    return [tag for _, tag in items]
