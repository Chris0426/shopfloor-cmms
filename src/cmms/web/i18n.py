def translate(key: str, locale: str) -> str:
    """key → 該 locale 字串;缺則回退 en;再缺回 key 本身(讓漏譯可見、不 crash)。"""
    loc = locale if locale in CATALOG else DEFAULT_LOCALE
    return CATALOG[loc].get(key) or CATALOG[DEFAULT_LOCALE].get(key) or key


